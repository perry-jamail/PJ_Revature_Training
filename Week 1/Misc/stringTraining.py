string = " Hello World "
print(f"string: {string}")

# lower
print(f"lower string: {string.lower()}")

# upper
print(f"upper string: {string.upper()}")

# strip
print(f"strip string: {string.strip()}")

# lstrip
print(f"lstrip string: {string.lstrip()}")

# rstrip
print(f"rstrip string: {string.rstrip()}")

# replace
print(f"replace string: {string.replace('Hello','')}")

# split
print(f"split string: {string.split()}")

# join
print(f"join string: {string.join([string, string])}")

# find
print(f"find string: {string.find("Hello")}")

# index
print(f"index string: {string.index("World")}")
